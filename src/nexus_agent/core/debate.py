from dataclasses import dataclass, field

from nexus_agent.llm.base import LLMProvider, Message, Role

logger = logging.getLogger(__name__)


@dataclass
class ReviewerPersona:
    """Represents a specialized reviewer persona in the debate engine."""
    name: str
    focus_area: str
    system_prompt: str


@dataclass
class CritiqueFeedback:
    """Critique details from a specific reviewer."""
    reviewer_name: str
    score: int  # 0 to 100
    approved: bool
    issues: list[str] = field(default_factory=list)
    suggestions: list[str] = field(default_factory=list)
    comments: str = ""


@dataclass
class DebateVerdict:
    """Consensus verdict generated by the Judge agent after reviews."""
    consensus_score: int                   # 0 to 100 aggregated score
    final_approved: bool                   # True if consensus is achieved
    reviewer_scores: dict[str, int]        # Individual scores
    aggregated_issues: list[str]          # Union of critical/major issues
    consensus_summary: str                 # Markdown summary of consensus details
    recommendations: list[str]             # List of actionable refactoring items
    rounds_conducted: int = 1
    reworked_code: str | None = None


